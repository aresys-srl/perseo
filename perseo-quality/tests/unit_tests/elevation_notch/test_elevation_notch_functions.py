    def test_antenna_pattern_normalization(self) -> None:
        """Testing antenna_pattern_normalization"""
        normalized_pattern, elevation_angle_rad_at_max_pos = antenna_pattern_normalization(
            antenna_pattern=self._antenna_pattern,
            antenna_angles_rad=self._antenna_angles_rad,
        )
        np.testing.assert_allclose(
            elevation_angle_rad_at_max_pos, self._expected_elevation_angle_rad_at_max_pos, atol=1e-6, rtol=0
        )
        assert "linear_gain" in normalized_pattern
        assert "gain" not in normalized_pattern
        np.testing.assert_allclose(
            normalized_pattern.elevation_angles.data, self._antenna_pattern.elevation_angles.data, atol=1e-6, rtol=0
        )
        np.testing.assert_allclose(
            normalized_pattern.linear_gain.data.squeeze(),
            self._expected_normalized_linear_gain,
            atol=1e-8,
            rtol=0,
        )

    def test_profile_normalization(self) -> None:
        """Testing profile_normalization"""
        normalized_profile = profile_normalization(
            profile=self._antenna_profile,
            antenna_angles_rad=self._antenna_angles_rad,
            elevation_angle_rad_at_max_pos=self._expected_elevation_angle_rad_at_max_pos,
        )
        np.testing.assert_allclose(normalized_profile, self._expected_normalized_profile, atol=1e-8, rtol=0)

    def test_computing_parabolic_fit(self) -> None:
        """Testing computing_parabolic_fit"""
        parab_fit_axis, parab_fit, parab_minimum_rad, parab_coeffs = compute_parabolic_profile_fit(
            profile=self._expected_normalized_profile,
            off_boresight_angles_rad=self._antenna_angles_rad,
        )
        np.testing.assert_allclose(parab_fit_axis, self._expected_parabolic_fit_axis, atol=1e-8, rtol=0)
        np.testing.assert_allclose(parab_fit, self._expected_parabolic_fit, atol=1e-8, rtol=0)
        np.testing.assert_allclose(parab_minimum_rad, self._expected_parabola_minimum_rad, atol=1e-8, rtol=0)
        np.testing.assert_allclose(parab_coeffs, self._expected_parabola_coeffs, atol=1e-8, rtol=0)

    def test_antenna_pattern_pointing_mismatch(self) -> None:
        """Testing antenna_pattern_pointing_mismatch"""
        normalized_pattern, _ = antenna_pattern_normalization(
            antenna_pattern=self._antenna_pattern,
            antenna_angles_rad=self._antenna_angles_rad,
        )
        mispointing_error_rad, calibration_constant, noise_floor = antenna_pattern_pointing_mismatch(
            antenna_pattern=normalized_pattern,
            data_profile=self._expected_normalized_profile,
            antenna_angles_from_data_rad=self._antenna_angles_rad,
            noise_profile=self._noise_profile,
            initial_guess=[0, 1, min(self._expected_normalized_profile)],
        )
